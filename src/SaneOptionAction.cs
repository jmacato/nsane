namespace NSane
{
    /// <summary>
    /// The option action
    /// </summary>
    internal enum SaneOptionAction : uint
    {
        /// <summary>
        /// Gets the option value
        /// </summary>
        Get = 0,

        /// <summary>
        /// Sets the option
        /// </summary>
        Set = 1,

        /// <summary>
        /// Sets the option to automatic
        /// </summary>
        Automatic = 2
    }
}
